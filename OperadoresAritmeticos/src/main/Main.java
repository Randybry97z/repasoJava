package main;

public class Main {

	public static void main(String[] args) {
		// Operadores aritméticos
		
		int variableX = 50, variableY = 10;
		int resultado;
		
		resultado = variableX + variableY;
		System.out.println(resultado);
		
		resultado = variableX - variableY;
		System.out.println(resultado);
		
		resultado = variableX * variableY;
		System.out.println(resultado);
		
		resultado = variableX / variableY;
		System.out.println(resultado);
		
		
		//Incrementos y decrementos
		
		variableX ++;
		variableX --;
		

	}

}
